export const getTranslations = (language: Language) => {
    // Deep merge the English translations as a fallback for any missing keys in other languages.
    const base = translations.en;
    const lang = translations[language] || base;

    // A simple deep merge function
    const merge = (target: any, source: any) => {
        for (const key in source) {
            if (source.hasOwnProperty(key)) {
                if (source[key] instanceof Object && !Array.isArray(source[key])) {
                    if (!target[key]) {
                        Object.assign(target, { [key]: {} });
                    }
                    merge(target[key], source[key]);
                } else {
                    if (target[key] === undefined) {
                        Object.assign(target, { [key]: source[key] });
                    }
                }
            }
        }
        return target;
    }

    if (language !== 'en') {
        return merge(JSON.parse(JSON.stringify(lang)), base);
    }
    
    return lang;
};